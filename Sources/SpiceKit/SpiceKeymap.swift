import Foundation

/// Maps macOS virtual keycodes (`NSEvent.keyCode`, the kVK_* constants) to PC XT
/// set-1 scancodes that the SPICE inputs channel expects. Extended keys are
/// encoded with 0xE0 in the high byte (e.g. 0xE048 = Up arrow); the shim splits
/// them into the 0xE0 prefix + low byte.
enum SpiceKeymap {
    static func scancode(forMacKeyCode keyCode: UInt16) -> UInt32? { table[keyCode] }

    /// macOS modifier keycode → the bit in `NSEvent.ModifierFlags.rawValue`-ish
    /// sense isn't used; instead we just need the scancode, which `table` covers.
    static let modifierKeyCodes: Set<UInt16> = [
        0x37, 0x36, 0x38, 0x3C, 0x3A, 0x3D, 0x3B, 0x3E, 0x39,
    ]

    private static let table: [UInt16: UInt32] = [
        // Letters
        0x00: 0x1E, 0x0B: 0x30, 0x08: 0x2E, 0x02: 0x20, 0x0E: 0x12,
        0x03: 0x21, 0x05: 0x22, 0x04: 0x23, 0x22: 0x17, 0x26: 0x24,
        0x28: 0x25, 0x25: 0x26, 0x2E: 0x32, 0x2D: 0x31, 0x1F: 0x18,
        0x23: 0x19, 0x0C: 0x10, 0x0F: 0x13, 0x01: 0x1F, 0x11: 0x14,
        0x20: 0x16, 0x09: 0x2F, 0x0D: 0x11, 0x07: 0x2D, 0x10: 0x15,
        0x06: 0x2C,
        // Numbers
        0x12: 0x02, 0x13: 0x03, 0x14: 0x04, 0x15: 0x05, 0x17: 0x06,
        0x16: 0x07, 0x1A: 0x08, 0x1C: 0x09, 0x19: 0x0A, 0x1D: 0x0B,
        // Symbols
        0x1B: 0x0C, 0x18: 0x0D, 0x21: 0x1A, 0x1E: 0x1B, 0x29: 0x27,
        0x27: 0x28, 0x2A: 0x2B, 0x2B: 0x33, 0x2F: 0x34, 0x2C: 0x35,
        0x32: 0x29,
        // Whitespace / control
        0x24: 0x1C,        // Return
        0x30: 0x0F,        // Tab
        0x31: 0x39,        // Space
        0x33: 0x0E,        // Backspace (Delete)
        0x35: 0x01,        // Escape
        0x39: 0x3A,        // Caps Lock
        // Modifiers
        0x38: 0x2A,        // Left Shift
        0x3C: 0x36,        // Right Shift
        0x3B: 0x1D,        // Left Control
        0x3E: 0xE01D,      // Right Control
        0x3A: 0x38,        // Left Option → Alt
        0x3D: 0xE038,      // Right Option → AltGr
        0x37: 0xE05B,      // Left Command → Left GUI
        0x36: 0xE05C,      // Right Command → Right GUI
        // Function keys
        0x7A: 0x3B, 0x78: 0x3C, 0x63: 0x3D, 0x76: 0x3E, 0x60: 0x3F,
        0x61: 0x40, 0x62: 0x41, 0x64: 0x42, 0x65: 0x43, 0x6D: 0x44,
        0x67: 0x57, 0x6F: 0x58,
        // Navigation (extended)
        0x7E: 0xE048,      // Up
        0x7D: 0xE050,      // Down
        0x7B: 0xE04B,      // Left
        0x7C: 0xE04D,      // Right
        0x73: 0xE047,      // Home
        0x77: 0xE04F,      // End
        0x74: 0xE049,      // Page Up
        0x79: 0xE051,      // Page Down
        0x75: 0xE053,      // Forward Delete
        0x72: 0xE052,      // Insert / Help
        // Keypad
        0x52: 0x52, 0x53: 0x4F, 0x54: 0x50, 0x55: 0x51, 0x56: 0x4B,
        0x57: 0x4C, 0x58: 0x4D, 0x59: 0x47, 0x5B: 0x48, 0x5C: 0x49,
        0x41: 0x53, 0x43: 0x37, 0x45: 0x4E, 0x4E: 0x4A, 0x4B: 0xE035,
        0x4C: 0xE01C,      // Keypad Enter
    ]
}
